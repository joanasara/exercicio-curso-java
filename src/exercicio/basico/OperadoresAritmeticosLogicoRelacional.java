package exercicio.basico;

public class OperadoresAritmeticosLogicoRelacional {

	public static void main(String[] args) {

		// ADIÇAO ==> SOMA

		int soma = 1 + 2;
		System.out.println(soma);

		soma = soma - 1;
		System.out.println(soma);

		soma = soma + 2;
		System.out.println(soma);

		soma = soma * 2;
		System.out.println(soma);

		soma = soma / 2;
		System.out.println(soma);

		soma = soma + 8;
		System.out.println(soma);

		soma = soma % 7;
		System.out.println(soma);
		
		//STRING PODE SE CONCATENAR EXEMPLO
		String primeiroNome = "joana";
		String segundoNome = "leite";
		String nomeJuntos = primeiroNome + segundoNome;
		System.out.println("Seu nome completo: " + nomeJuntos);
		
		
		

	}

}
